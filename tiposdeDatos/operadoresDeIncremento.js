let a=3, b=2;

let z=a+b;

//Incremento
//Pre-incremento(el operador ++ antes de la variable)
z=++a;
console.log(a);
console.log(z);

//Pos incremento se incrementa la proxima vez que se va  a utilizar
z=b++;
console.log(b);
console.log(z);

//Decremento

//Pre decremento
z=--a;
console.log(a);
console.log(z);

//Pos decremento se decrementa la proxima vez que se utiliza
z=b--;
console.log(b);
console.log(z);

